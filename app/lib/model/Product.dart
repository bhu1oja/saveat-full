class Product {
  final String id;
  final String name;
  final String slug;
  final String description;
  final String price;
  final String discount;
  final String bestBuyDate;
  final String productImage;
  final String category;
  final String vendorID;

  Product(
      this.id,
      this.name,
      this.slug,
      this.description,
      this.price,
      this.discount,
      this.bestBuyDate,
      this.productImage,
      this.category,
      this.vendorID);
}
